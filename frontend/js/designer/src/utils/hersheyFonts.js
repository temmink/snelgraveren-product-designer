export const ENGRAVING_FONTS = [
  { id: "hershey-simplex", label: "Simplex (Sans-Serif)", glyphs: simplexGlyphs },
  { id: "hershey-script",  label: "Script (Cursive)",     glyphs: scriptGlyphs },
  { id: "hershey-gothic",  label: "Gothic (Bold)",        glyphs: gothicGlyphs },
];

/**
 * Render a string as a combined SVG path using a Hershey font.
 *
 * @param {string} text      - The text to render
 * @param {string} fontId    - One of the ENGRAVING_FONTS ids
 * @param {Object} [options]
 * @param {number} [options.fontSize=24]  - Desired font size in px
 * @param {number} [options.letterSpacing=1] - Extra spacing between characters
 * @returns {{ d: string, width: number, height: number }}
 */
export function renderHersheyText(text, fontId, options = {}) {
  const font = ENGRAVING_FONTS.find((f) => f.id === fontId);
  if (!font) return { d: "", width: 0, height: 0 };

  const { fontSize = 24, letterSpacing = 1 } = options;
  // Hershey glyphs are designed on a ~25-unit em square (y: 0..25)
  const emHeight = 25;
  const scale = fontSize / emHeight;

  let x = 0;
  const segments = [];

  for (let i = 0; i < text.length; i++) {
    const code = text.charCodeAt(i);
    const glyph = font.glyphs[code] || font.glyphs[63]; // fallback to "?"
    if (!glyph) continue;

    if (glyph.d) {
      // Offset the glyph path by current x position
      const shifted = offsetPath(glyph.d, x, 0);
      segments.push(shifted);
    }

    x += (glyph.w || 16) + letterSpacing;
  }

  const totalWidth = x * scale;
  const totalHeight = fontSize;

  // Combine all segments and apply scale
  const combined = segments.join(" ");
  const scaled = scalePath(combined, scale);

  return { d: scaled, width: totalWidth, height: totalHeight };
}

/**
 * Offset all coordinates in a simple SVG path string by (dx, dy).
 */
function offsetPath(d, dx, dy) {
  return d.replace(/(-?\d+\.?\d*)\s+(-?\d+\.?\d*)/g, (_, xStr, yStr) => {
    const x = parseFloat(xStr) + dx;
    const y = parseFloat(yStr) + dy;
    return x + " " + y;
  });
}

/**
 * Scale all coordinates in a simple SVG path string by a factor.
 */
function scalePath(d, s) {
  return d.replace(/(-?\d+\.?\d*)/g, (_, num) => {
    return (parseFloat(num) * s).toFixed(2);
  });
}
